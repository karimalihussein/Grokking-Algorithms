<?php

namespace App\LinkedLists;

class Box
{
    public $data;
    public $next;

    public function __construct($data)
    {
        $this->data = $data;
    }

    public function getData()
    {
        return $this->data;
    }

    public function setData($data)
    {
        $this->data = $data;
    }

    public function getNext()
    {
        return $this->next;
    }

    public function setNext($next)
    {
        $this->next = $next;
    }

    public function getValue()
    {
        return $this->data;
    }

    


}